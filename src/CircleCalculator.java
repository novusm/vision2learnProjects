import java.util.Scanner;
public class CircleCalculator {
    public static void main(String[] args) {

        double circleRadius = 12.0;
        double circleDiameter = circleRadius * 2;
        double circleCumference = circleDiameter * 3.14;
        double circleArea = (circleRadius * 2)*2;

        System.out.println(circleDiameter);
        System.out.println(circleCumference);
        System.out.println(circleArea);
        System.out.println(circleCumference/circleDiameter);







    }
}
